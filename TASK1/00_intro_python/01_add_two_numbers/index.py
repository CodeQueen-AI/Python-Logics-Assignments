def main():
    # Ask the user to enter the first number
    a = int(input("Enter first number: "))

    # Ask the user to enter the second number
    b = int(input("Enter second number: "))

    # Calculate the sum
    result = a + b

    # Show the result
    print(f"The sum of {a} and {b} is: {result}")

# Call the main function
main()
